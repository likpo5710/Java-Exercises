
public class MathAndNumbers {

	public static void main(String[] args) {
		// Initial sentence, counting the chickens
		System.out.println( "I will now count my chickens:" );
		// There are 30 Hens
		System.out.println( "Hens " + ( 25 + 30 / 6 ) );
		// There are 97 Roosters
		System.out.println( "Roosters " + ( 100 - 25 * 3 % 4 ) );
		// Sentence for counting the eggs
		System.out.println( "Now I will count the eggs:" );
		//Sentence output as an equation
		System.out.println( 3 + 2 + 1 - 5 + 4 % 2 - 1 / 4 + 6 );
		//Sentence output as a boolean question
		System.out.println( "Is it true that 3 + 2 < 5 - 7?" );
		//Sentence output as a True or False statement
		System.out.println( 3 + 2 < 5 - 7 );
		//Sentence output as a math question
		System.out.println( "What is 3 + 2? " + ( 3 + 2 ) );
		//Sentence output as second math question
		System.out.println( "What is 5 - 7? " + ( 5 - 7 ) );
		//Sentence output for realization of previous boolean statement
		System.out.println( "Oh, that's why it's false." );
		//Sentence output as a question
		System.out.println( "How about some more." );
		//Sentence output as greater than question
		System.out.println( "Is it greater? " + ( 5 > -2 ) );
		//Sentence output as greater or equal than question
		System.out.println( "Is it greater or equal? " + ( 5 >= -2 ) );
		//Sentence output as less or equal than question
		System.out.println( "Is it less or equal? " + ( 5 <= -2 ) );

	}

}
